public class PrettyPrinting{
    public static void main(String[] args) {
        float a = 12.3394f;
        System.out.printf("Floating number is %.2f",a);
        System.out.printf("PI is %.3f",Math.PI);
        System.out.printf("My name is %s and I am %s","Monisha","Cool");
    
    }
}