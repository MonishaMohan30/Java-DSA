public class SwitchCaseStatements{
    
    private static String fruit = "Apple";
 
    public static void main (String args[])
    {
        switch (fruit){
        case "Apple" :
         System.out.println("Hi Apple");
         break;
        case "Mango" :
        System.out.println("Hi Mango");
        break;
        default :
        System.out.println("poi thola");
        break;

    }
 }
}